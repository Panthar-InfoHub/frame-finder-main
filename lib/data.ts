// primitives
export type ObjectId = string
export type ISODateString = string
export type CurrencyAmount = number

// top-level response
export interface ProductsResponse {
  products: Product[]
  pagination: Pagination
}

export interface Pagination {
  totalProducts: number
  totalPages: number
}

// product entity (keys unchanged)
export interface Product {
  _id: ObjectId
  productCode: string
  brand_name: string
  material: string[]
  shape: string[]
  style: string[]
  hsn_code: string
  sizes: string[]
  gender: string[]
  vendorId: ObjectId
  rating?: number
  total_reviews?: number
  status: "active" | "inactive" | string // keep open for future statuses
  is_Power: boolean
  type: "Product" | string
  variants: Variant[]
  createdAt: ISODateString
  updatedAt: ISODateString
  __v: number

  // only present on some items
  dimension?: Dimension
}

// optional nested dimension block
export interface Dimension {
  lens_width: string
  bridge_width: string
  temple_length: string
  lens_height: string
}

// variant entity
export interface Variant {
  frame_color: string
  temple_color: string
  price: Price
  images: ImageItem[]
  stock: Stock
  _id: ObjectId
}

// price breakdown (keys unchanged)
export interface Price {
  base_price: CurrencyAmount
  mrp: CurrencyAmount
  shipping_price: ShippingPrice
  total_price: CurrencyAmount
}

export interface ShippingPrice {
  custom: boolean
  value: CurrencyAmount
}

// image item (keys unchanged)
export interface ImageItem {
  url: string
  _id: ObjectId
}

// stock info
export interface Stock {
  current: number
  minimum: number
}

